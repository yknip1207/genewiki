package org.gnf.wikiapi;

/**
 * Manages error data from the <a href="http://meta.wikimedia.org/w/api.php">Wikimedia API</a>
 */
public class ErrorData {

    private String code;
    private String info;


    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getInfo() {
        return info;
    }

    public void setInfo(String info) {
        this.info = info;
    }

    @Override
    public String toString() {
        return "ErrorData{" +
                "code='" + code + '\'' +
                ", info='" + info + '\'' +
                '}';
    }
}
